main:Toggle2("Auto Sell",function()
  top,topdis = nil,100
  for i,v in pairs(workspace.SellPlaces:GetChildren()) do
    if(mag(v) < topdis) then
      topdis = mag(v)
      top = v
    end
  end
  if(top) then
    game:GetService("ReplicatedStorage").Events.Click:FireServer({"Screen",top})
  end
end)

